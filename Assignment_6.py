# creating a tuple
tup = (10, 20, 30, 40, 30, 50)
print("The tuple is: ")
print(tup)
# Find the index of number 40
ind = tup.index(40)
print("The index of number 40 :")
print(ind)

# Count for the number 30
print("The count for the number 30 :")
ct = tup.count(30)
print(ct)

# Delete the tuple
print("Deleting the tuple")
del tup
