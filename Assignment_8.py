'''Create the following sets Set A={1,2,3,4,5}
Set B={4,5,6,7,8} Perform the following operations on the above sets.
1. Intersection
2. Union
3. Difference
4. Symmetric difference'''

# creating sets A and B
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

# intersection
print("The intersection of A and B :", A & B)

# union
print("The union of A and B:", A | B)

# difference
print("The difference of A and B: ", A - B)

# symmetric difference
print("The symmetric difference of A and B: ", A ^ B)


