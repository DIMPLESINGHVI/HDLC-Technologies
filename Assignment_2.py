# calculate the sum and average of three numbers
n1 = int(input("Enter the value for number 1: "))
n2 = int(input("Enter the value for number 2: "))
n3 = int(input("Enter the value for number 3: "))

# calculate the sum
s = n1 + n2 + n3
print("The sum of numbers ", n1, n2, n3, "is ", s)

# calculate the average
a = float(s/3)
print("The average of the numbers ", n1, n2, n3, "is ", a)


